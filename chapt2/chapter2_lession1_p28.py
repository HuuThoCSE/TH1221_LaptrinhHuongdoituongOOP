# 1. Viết chương trình nhập vào 2 số phức, cho biết
# kết quả của 4 phép toán cộng, trừ, nhân, chia trên 2 số phức đó.

z1 = complex(input("Nhập vào 2 số phức: "))
z2 = complex(input())
print("Cộng 2 số phức: ", z1 + z2)
print("Trừ 2 số phức: ", z1 - z2)
print("Nhân 2 số phức: ", z1 * z2)
print("Chia 2 số phức: ", z1 / z2)